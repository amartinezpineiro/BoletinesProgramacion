package Boletines.Boletin15;

public class Multimedia {
    private String titulo;
    private String autor;
    private String formato;
    private double duracion;

    @Override
    public String toString() {
        return "Multimedia{" +
                "titulo='" + titulo + '\'' +
                ", autor='" + autor + '\'' +
                ", formato='" + formato + '\'' +
                ", duracion=" + duracion +
                '}';
    }
}
